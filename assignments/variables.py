# You can use comments in your applications with # or """ """

# With strings you can either use single or double quotes
mystring = 'hello'
print(mystring)
mystring = "hello"
print(mystring)

# A floating point number can be created in two ways:
myfloat = 7.0
print(myfloat)
myfloat = float(7)
print(myfloat)

